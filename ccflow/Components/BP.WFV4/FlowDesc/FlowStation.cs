namespace BP.WF
{
    /// <summary>
    /// 流程岗位属性属性	  
    /// </summary>
    public class FlowStationAttr
    {
        /// <summary>
        /// 节点
        /// </summary>
        public const string FK_Flow = "FK_Flow";
        /// <summary>
        /// 工作岗位
        /// </summary>
        public const string FK_Station = "FK_Station";
    }
    /// <summary>
    /// 流程岗位属性
    /// 节点的工作岗位有两部分组成.	 
    /// 记录了从一个节点到其他的多个节点.
    /// 也记录了到这个节点的其他的节点.
    /// </summary>
    public class FlowStation : EntityMM
    {
        #region 基本属性
        /// <summary>
        /// HisUAC
        /// </summary>
        public override UAC HisUAC
        {
            get
            {
                UAC uac = new UAC();
                uac.OpenForSysAdmin();
                return uac;
            }
        }
        /// <summary>
        ///节点
        /// </summary>
        public string FK_Flow
        {
            get
            {
                return this.GetValStringByKey(FlowStationAttr.FK_Flow);
            }
            set
            {
                this.SetValByKey(FlowStationAttr.FK_Flow, value);
            }
        }
        /// <summary>
        /// 工作岗位
        /// </summary>
        public string FK_Station
        {
            get
            {
                return this.GetValStringByKey(FlowStationAttr.FK_Station);
            }
            set
            {
                this.SetValByKey(FlowStationAttr.FK_Station, value);
            }
        }
        #endregion

        #region 构造方法
        /// <summary>
        /// 流程岗位属性
        /// </summary>
        public FlowStation() { }
        /// <summary>
        /// 重写基类方法
        /// </summary>
        public override Map EnMap
        {
            get
            {
                if (this._enMap != null)
                    return this._enMap;

                Map map = new Map("WF_FlowStation");
                map.EnDesc = "流程岗位属性信息";

                map.DepositaryOfEntity = Depositary.None;
                map.DepositaryOfMap = Depositary.Application;

                map.AddDDLEntitiesPK(FlowStationAttr.FK_Flow, null, "FK_Flow", new Flows(), true);
                map.AddDDLEntitiesPK(FlowStationAttr.FK_Station, null, "工作岗位", new Stations(), true);
                this._enMap = map;

                return this._enMap;
            }
        }
        #endregion
    }
    /// <summary>
    /// 流程岗位属性
    /// </summary>
    public class FlowStations : EntitiesMM
    {
        /// <summary>
        /// 他的工作岗位
        /// </summary>
        public Stations HisStations
        {
            get
            {
                Stations ens = new Stations();
                foreach (FlowStation ns in this)
                {
                    ens.AddEntity(new Station(ns.FK_Station));
                }
                return ens;
            }
        }
        /// <summary>
        /// 他的工作节点
        /// </summary>
        public Nodes HisNodes
        {
            get
            {
                Nodes ens = new Nodes();
                foreach (FlowStation ns in this)
                {
                    ens.AddEntity(new Node(ns.FK_Flow));
                }
                return ens;

            }
        }
        /// <summary>
        /// 流程岗位属性
        /// </summary>
        public FlowStations() { }
        /// <summary>
        /// 流程岗位属性
        /// </summary>
        /// <param name="NodeID">节点ID</param>
        public FlowStations(int NodeID)
        {
            QueryObject qo = new QueryObject(this);
            qo.AddWhere(FlowStationAttr.FK_Flow, NodeID);
            qo.DoQuery();
        }
        /// <summary>
        /// 流程岗位属性
        /// </summary>
        /// <param name="StationNo">StationNo </param>
        public FlowStations(string StationNo)
        {
            QueryObject qo = new QueryObject(this);
            qo.AddWhere(FlowStationAttr.FK_Station, StationNo);
            qo.DoQuery();
        }
        /// <summary>
        /// 得到它的 Entity 
        /// </summary>
        public override Entity GetNewEntity
        {
            get
            {
                return new FlowStation();
            }
        }
        /// <summary>
        /// 取到一个工作岗位集合能够访问到的节点s
        /// </summary>
        /// <param name="sts">工作岗位集合</param>
        /// <returns></returns>
        public Nodes GetHisNodes(Stations sts)
        {
            Nodes nds = new Nodes();
            Nodes tmp = new Nodes();
            foreach (Station st in sts)
            {
                tmp = this.GetHisNodes(st.No);
                foreach (Node nd in tmp)
                {
                    if (nds.Contains(nd))
                        continue;
                    nds.AddEntity(nd);
                }
            }
            return nds;
        }
        /// <summary>
        /// 取到一个工作人员能够访问到的节点。
        /// </summary>
        /// <param name="empId">工作人员ID</param>
        /// <returns></returns>
        public Nodes GetHisNodes_del(string empId)
        {
            Emp em = new Emp(empId);
            return this.GetHisNodes(em.HisStations);
        }
        /// <summary>
        /// 工作岗位对应的节点
        /// </summary>
        /// <param name="stationNo">工作岗位编号</param>
        /// <returns>节点s</returns>
        public Nodes GetHisNodes(string stationNo)
        {
            QueryObject qo = new QueryObject(this);
            qo.AddWhere(FlowStationAttr.FK_Station, stationNo);
            qo.DoQuery();

            Nodes ens = new Nodes();
            foreach (FlowStation en in this)
            {
                ens.AddEntity(new Node(en.FK_Flow));
            }
            return ens;
        }
        /// <summary>
        /// 转向此节点的集合的Nodes
        /// </summary>
        /// <param name="nodeID">此节点的ID</param>
        /// <returns>转向此节点的集合的Nodes (FromNodes)</returns> 
        public Stations GetHisStations(int nodeID)
        {
            QueryObject qo = new QueryObject(this);
            qo.AddWhere(FlowStationAttr.FK_Flow, nodeID);
            qo.DoQuery();

            Stations ens = new Stations();
            foreach (FlowStation en in this)
            {
                ens.AddEntity(new Station(en.FK_Station));
            }
            return ens;
        }
    }
